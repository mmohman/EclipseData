package com.class03;

public class TaskNumbers {

	public static void main(String[] args) {
	
		//positive or number
		int a=-25;
		if (a>=0) {
			System.out.println(a+" is positive number");
		}else {
			System.out.println(a+" is negative number");
		}
		
		
		//even or odd
		int od=16;
		if (od % 2 == 0) {
			System.out.println(od+" is even number");
		}else {
			System.out.println(od+" is odd number");
		}
	}

}
